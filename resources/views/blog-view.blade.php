                                                <p>Around the world, entrepreneurship has been a male-dominated phenomenon from a very early age, but time has changed significantly. In today's India, the role of women entrepreneurs in economic growth is inevitable, and they have become an inalienable part of the Indian startup ecosystem. At the institutional level, several steps are being taken by the government to encourage women to pursue their entrepreneurial dream and make them a reality.Realizing the importance of mentorship, assistance in setting up a business, a steady flow of funding, and access to expert guidance at the initial stage of business operation, the government of India has launched several initiatives that will benefit women in their entrepreneurial journey.</p>

                                                <p>In this article, we will learn some of the key initiatives launched by different departments of the government of India to support women in entrepreneurship. If you are an aspiring women entrepreneur, this article will help you step in the right direction towards your goal.Let's begin. </p>

                                                <div class="my-2">
                                                    <div class="secondary_text">
                                                        <h3>Pradhan Mantri Mudra Yojana (PMMY):</h3>

                                                        <p>Since the launch of the Mudra refinances loan scheme in 2015, it has been one of the well-known financial assistance initiatives amongst Indian entrepreneurs. It focuses on Micro, Small and Medium enterprises (MSMEs) in India. The scheme stands true to its goal of funding the unfunded section of the Indian business ecosystem.</p>

                                                        <p>The loan scheme provides financial assistance in the form of collateral-free credit at a low-interest rate.</p>

                                                        <p>Under the MUDRA loan scheme, the applicant business will get financial credit assistance in any one of the three forms:</p>

                                                        <ul>
                                                            <li>Shishu: covering loans up to Rs. 50,000/-</li>
                                                            <li>Kishor: covering loans above Rs. 50,000/- and up to Rs. 5 lakh</li>
                                                            <li>Tarun: covering loans above Rs. 5 lakh to Rs. 10 lakh</li>
                                                        </ul>

                                                        <p>Aspiring women entrepreneurs can reach out to banks, NBFCs, and Microfinance Institutions (MFIs) to avail the MUDRA refinance loans. Borrowing persons can submit their online application on the Udyamimtra portal.</p>

                                                    </div>
                                                </div>

                                                <div class="my-2">
                                                    <div class="secondary_text">
                                                        <h3>Dena Shakti Scheme:</h3>

                                                        <p>Dena Shakti Scheme is designed to cater to the requirement of women entrepreneurs looking for affordable financing in the form of a loan. The scheme covers a wide range of business sectors and activities. Some of them are:</p>

                                                        <ul class="numbers">
                                                            <li>All micro and small manufacturing enterprises</li>
                                                            <li>Retail trade</li>
                                                            <li>All allied activities in agriculture.</li>
                                                            <li>Education</li>
                                                            <li>Micro Credit</li>
                                                            <li>Micro and small enterprises engaged in the service sector, which includes water transport operators</li>
                                                            <li>Other small business operators, self-employed, and all other service enterprises.</li>
                                                            <li>Housing</li>
                                                            <li>Small enterprises in (Direct and Indirect finance)</li>
                                                        </ul>

                                                        <p>Any women entrepreneurs who are already engaged or planning to start a business in any of the business activities mentioned above can use this scheme. To be eligible for this scheme, female entrepreneurs should hold equal to or more than 51% shareholding stake in the company.</p>

                                                        <p>As per the current guidelines of the Bank and directives issued by the RBI, the Interest rate on loans disbursed under Dena Shakti Scheme will be determined.If the woman in the business holds a 51% stake in the company, then the lending bank will offer 0.25% lower interest on the loan amount and the loan tenure is up to 7 years. The maximum loan amount available under this scheme is different for different business activities.</p>

                                                        <p>For a women entrepreneur in agriculture and allied activities, manufacturing, retail trading, or small enterprises (Direct and Indirect Finance), the maximum loan amount is Rs. 20 lakhs.</p>

                                                    </div>
                                                </div>
                                                <div class="my-2">
                                                    <div class="secondary_text">
                                                        <h3>Conclusion: </h3>
                                                        <p>Currently, there are more than 15.7 million women-owned enterprises. For the past few years, many women with entrepreneurial aspiration are coming forward to realize their dream and making into reality. These women-led ventures contribute to the Indian economy and create more job opportunities. They also play a unique role in inspiring other women in the country to take up entrepreneurship. As we've reached the end of this article, we hope you have some clarity on the financial assistance available for women entrepreneurs that will enable your business to scale.</p>

